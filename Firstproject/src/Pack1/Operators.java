package Pack1;

public class Operators {
	
	
	public static void main(String[] args) {
	
		int a = 60;
		System.out.println(a);
		
		int b = ++a;
		System.out.println(b);
		
		int c = a++;
		System.out.println(c);
		System.out.println(a);
		
		int d = a--;
		System.out.println(d);
		System.out.println(a);
		
		int e = --a;
		System.out.println(e);
	}

}
